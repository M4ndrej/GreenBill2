    private void jButtonDetaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDetaljiActionPerformed
        int red = jTableGJ.getSelectedRow();
        if (red == -1) {
            JOptionPane.showMessageDialog(this, "Izaberite gazdinsku jedinicu", "Greška", JOptionPane.ERROR_MESSAGE);
            return;
        }
        GazdinskaJedinicaModelTabele gjmt = (GazdinskaJedinicaModelTabele) jTableGJ.getModel();
        GazdinskaJedinica gj = gjmt.getLista().get(red);
        GazdinskaJedinicaDialog gjd = new GazdinskaJedinicaDialog(parent, true, gj);
        gjd.setVisible(true);
    }//GEN-LAST:event_jButtonDetaljiActionPerformed



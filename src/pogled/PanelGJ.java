    private void inicijalizacija() {
        List<GazdinskaJedinica> lista = new ArrayList<>();
        boolean uspesno = Controller.getInstance().vratiListuGazdinskaJedinica(lista);
        if (uspesno) {
            GazdinskaJedinicaModelTabele gjmt = new GazdinskaJedinicaModelTabele(lista);
            jTableGJ.setModel(gjmt);
        } else {
            JOptionPane.showMessageDialog(this, "Sistem ne može da učita listu gazdinskih jedinica", "Greška", JOptionPane.ERROR_MESSAGE);
        }
    }



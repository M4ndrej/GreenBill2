    private boolean proveriComboBox() {
        int count = 0;
        if (jComboBoxLokalitet.getSelectedItem() != null) {
            count++;
        }
        if (jComboBoxGazdinskaJedinica.getSelectedItem() != null) {
            count++;
        }
        if (jComboBoxOdeljenjeOdsek.getSelectedItem() != null) {
            count++;
        }
        if (count == 0 || count == 1) {
            return true;
        }
        return false;
    }


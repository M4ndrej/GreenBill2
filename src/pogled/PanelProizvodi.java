    private void jButtonDetalji2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDetalji2ActionPerformed
        int red = jTableProizvodi.getSelectedRow();
        if (red == -1) {
            JOptionPane.showMessageDialog(this, "Izaberite proizvod", "Gre≈°ka", JOptionPane.ERROR_MESSAGE);
            return;
        }
        ProizvodModelTabele pmt = (ProizvodModelTabele) jTableProizvodi.getModel();
        Proizvod proizvod = pmt.getLista().get(red);
        ProizvodDialog pd = new ProizvodDialog(parent, true, proizvod);
        pd.setVisible(true);
    }//GEN-LAST:event_jButtonDetalji2ActionPerformed


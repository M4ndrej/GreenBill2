    private void jButtonSacuvajIzmeneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSacuvajIzmeneActionPerformed
        if (jTextFieldDoznaka.getText().isEmpty()
                || !jTextFieldDoznaka.getText().matches("\\d+(\\.\\d+)?")
                || jTextFieldDatumDoznake.getText().isEmpty()) {
            jLabelNazivError.setText(jTextFieldNaziv.getText().isEmpty() ? "Unesite odsek i odeljenje" : "");
            jLabelDoznakaError.setText(jTextFieldDoznaka.getText().isEmpty() ? "Unesite doznaku"
                    : !jTextFieldDoznaka.getText().matches("\\d+(\\.\\d+)?") ? "Unesite broj" : "");
            return;
        }
        GazdinskaJedinica gj = (GazdinskaJedinica) jComboBoxJG.getSelectedItem();
        String naziv = jTextFieldNaziv.getText();
        double novaDoznaka = Double.parseDouble(this.jTextFieldDoznaka.getText());
        SimpleDateFormat format = new SimpleDateFormat("dd.MM.yyyy.");
        java.util.Date datum = null;

        try {
            datum = format.parse(this.jTextFieldDatumDoznake.getText());
        } catch (ParseException ex) {
            Logger.getLogger(OdeljenjeOdsekDialog.class.getName()).log(Level.SEVERE, null, ex);
        }

        oo.setNaziv(naziv);
        oo.setGazdinskaJedinica(gj);
        oo.setDoznaka(novaDoznaka);
        oo.setDatumDoznake(datum);

        boolean uspesno = Controller.getInstance().izmeniOdeljenjeOdsek(oo);
//        if (staraDoznaka != novaDoznaka) {
//            try {
//                Email.promeniDoznaku(staraDoznaka, novaDoznaka, Controller.getInstance().getUlovovani(), oo);
//            } catch (MessagingException ex) {
//                Logger.getLogger(OdeljenjeOdsekDialog.class.getName()).log(Level.SEVERE, null, ex);
//            }
//        }
        if (!uspesno) {
            JOptionPane.showMessageDialog(this, "Greška prilikom izmene odeljenja i odseka", "Greška", JOptionPane.ERROR_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Uspešno izmenjeno mesto", "Uspešno", JOptionPane.INFORMATION_MESSAGE);
            parent.azurirajTabelu("oo");
            this.dispose();
        }

    }//GEN-LAST:event_jButtonSacuvajIzmeneActionPerformed

    private void jButtonKreirajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonKreirajActionPerformed
        this.setValidationLabels();
        if (jTextFieldDoznaka.getText().isEmpty()
                || !jTextFieldDoznaka.getText().matches("\\d+(\\.\\d+)?")
                || jTextFieldDatumDoznake.getText().isEmpty()) {
            jLabelNazivError.setText(jTextFieldNaziv.getText().isEmpty() ? "Unesite odsek i odeljenje" : "");
            jLabelDoznakaError.setText(jTextFieldDoznaka.getText().isEmpty() ? "Unesite doznaku"
                    : !jTextFieldDoznaka.getText().matches("\\d+(\\.\\d+)?") ? "Unesite broj" : "");
            return;
        }

        GazdinskaJedinica gj = (GazdinskaJedinica) jComboBoxJG.getSelectedItem();
        String naziv = jTextFieldNaziv.getText();
        double novaDoznaka = Double.parseDouble(this.jTextFieldDoznaka.getText());
        SimpleDateFormat format = new SimpleDateFormat("dd.MM.yyyy.");
        java.util.Date datum = null;

        try {
            datum = format.parse(this.jTextFieldDatumDoznake.getText());
        } catch (ParseException ex) {
            Logger.getLogger(OdeljenjeOdsekDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
        oo = new OdeljenjeOdsek();
        oo.setNaziv(naziv);
        oo.setGazdinskaJedinica(gj);
        oo.setDoznaka(novaDoznaka);
        oo.setDatumDoznake(datum);
        boolean uspesno = Controller.getInstance().kreirajOdeljenjeOdsek(oo);
        if (uspesno) {
            JOptionPane.showMessageDialog(this, "Odeljenje i odsek uspešno kreirani", "Obaveštenje", JOptionPane.INFORMATION_MESSAGE);
            parent.azurirajTabelu("oo");
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Greška prilikom kreiranja odeljenja i odseka", "Greška", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButtonKreirajActionPerformed


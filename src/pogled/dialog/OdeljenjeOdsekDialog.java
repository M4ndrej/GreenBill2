    private void inicijalizacija() {
        jComboBoxJG.setSelectedItem(null);
        jButtonSacuvajIzmene.setVisible(false);
        jButtonIzmeni.setVisible(false);
        jButtonObrisi.setVisible(false);
        java.util.Date datum = new java.util.Date();
        SimpleDateFormat format = new SimpleDateFormat("dd.MM.yyyy.");
        String datumString = format.format(datum);
        jTextFieldDatumDoznake.setText(datumString);
        jTextFieldDatumDoznake.setEnabled(false);

        this.setValidationLabels();

    }

    private void inicijalizacija(OdeljenjeOdsek oo) {

        jComboBoxJG.setSelectedItem(oo.getGazdinskaJedinica());
        jComboBoxJG.setEnabled(false);
        jButtonKreiraj.setVisible(false);
        jButtonSacuvajIzmene.setEnabled(false);
        jTextFieldNaziv.setText(oo.getNaziv());
        jTextFieldDoznaka.setText(oo.getDoznaka() + "");
        SimpleDateFormat format = new SimpleDateFormat("dd.MM.yyyy.");
        try {
            jTextFieldDatumDoznake.setText(format.format(oo.getDatumDoznake()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Pogresan format datuma", "Greska", JOptionPane.ERROR_MESSAGE);
            return;
        }
        jTextFieldNaziv.setEnabled(false);
        jTextFieldDatumDoznake.setEnabled(false);
        jTextFieldDoznaka.setEnabled(false);
        jComboBoxJG.setEnabled(false);

        this.setValidationLabels();

    }

    private void setValidationLabels() {
        jLabelNazivError.setText("");
        jLabelDoznakaError.setText("");
        jLabelDatumDoznakeError.setText("");
    }

    private void popuniComboBox() {

        List<GazdinskaJedinica> listaGJ = new ArrayList<>();
        boolean uspesno2 = Controller.getInstance().vratiListuGazdinskaJedinica(listaGJ);
        if (uspesno2) {
            for (GazdinskaJedinica gj : listaGJ) {
                jComboBoxJG.addItem(gj);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Sistem ne može da učita listu gazdinskih jedinica", "Greška", JOptionPane.ERROR_MESSAGE);
        }
    }
